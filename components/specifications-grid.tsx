export function SpecificationsGrid() {
  const [selected, setSelected] = useState(specifications[0].id)

  const selectedSpec = specifications.find((s) => s.id === selected)

  return (
    <section className="py-8 px-4" dir="rtl" style={{ direction: "rtl", textAlign: "right" }}>
      <div className="max-w-6xl mx-auto">
        {/* Sub-sections */}
        <div className="grid grid-cols-3 md:grid-cols-4 lg:grid-cols-11 gap-2 mb-8" style={{ direction: "rtl" }}>
          {specifications.map((spec) => {
            const Icon = spec.icon
            const isActive = selected === spec.id
            return (
              <button
                key={spec.id}
                onClick={() => setSelected(spec.id)}
                className={`flex flex-col items-center gap-2 p-3 rounded-xl border transition-all ${
                  isActive
                    ? "bg-primary/10 border-primary text-primary"
                    : "bg-secondary border-border text-muted-foreground hover:border-muted-foreground"
                }`}
                style={{ direction: "rtl" }}
              >
                <Icon className="w-5 h-5" />
                <span className="text-xs font-medium text-center">{spec.name}</span>
              </button>
            )
          })}
        </div>

        {/* Details */}
        {selectedSpec && (
          <Card className="bg-card border-border p-6" style={{ direction: "rtl" }}>
            <div className="flex items-center gap-3 mb-6" style={{ direction: "rtl", justifyContent: "flex-start" }}>
              {(() => {
                const Icon = selectedSpec.icon
                return <Icon className="w-6 h-6 text-primary" />
              })()}
              <h2 className="text-xl font-bold text-primary">مواصفات {selectedSpec.name}</h2>
            </div>

            <ScrollArea className="h-[500px]" dir="rtl" style={{ direction: "rtl" }}>
              <div className="space-y-3 pr-4" style={{ direction: "rtl" }}>
                {selectedSpec.items.map((item, index) => (
                  <div key={index} className="flex gap-3 items-start" style={{ direction: "rtl", textAlign: "right" }}>
                    <span
                      className="flex-shrink-0 w-7 h-7 rounded-full bg-primary/20 text-primary text-xs flex items-center justify-center font-bold"
                      style={{ fontFamily: "inherit" }}
                    >
                      {index + 1}
                    </span>
                    <p
                      className="text-muted-foreground text-sm leading-relaxed flex-1"
                      style={{ textAlign: "right", direction: "rtl" }}
                    >
                      {item}
                    </p>
                  </div>
                ))}
              </div>
            </ScrollArea>
          </Card>
        )}
      </div>
    </section>
  )
}
