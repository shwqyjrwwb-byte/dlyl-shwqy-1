// دالة لتوليد الوصف الوظيفي بناءً على المسمى الوظيفي
export function getJobDescription(position: string): string {
  const positionLower = position.toLowerCase()
  
  // رئيس مجلس الإدارة
  if (positionLower.includes('رئيس مجلس الإدارة')) {
    return 'الإشراف العام على جميع عمليات الشركة، وضع الاستراتيجيات والخطط طويلة المدى، اتخاذ القرارات الاستراتيجية الكبرى، والإشراف على الأداء المالي والتشغيلي للشركة.'
  }
  
  // نائب رئيس مجلس الإدارة
  if (positionLower.includes('نائب رئيس')) {
    return 'مساعدة رئيس مجلس الإدارة في الإشراف على العمليات اليومية، المشاركة في وضع الاستراتيجيات، والإشراف على تنفيذ القرارات الإدارية.'
  }
  
  // المدير العام
  if (positionLower.includes('مدير عام') || positionLower.includes('المدير العام')) {
    return 'إدارة العمليات اليومية للشركة، تنسيق العمل بين الأقسام المختلفة، متابعة تنفيذ الخطط والأهداف، وضمان سير العمل بكفاءة.'
  }
  
  // مديرة مكتب
  if (positionLower.includes('مديرة مكتب') || positionLower.includes('مدير مكتب')) {
    return 'تنظيم وإدارة مكتب الإدارة العليا، تنسيق المواعيد والاجتماعات، متابعة المراسلات والمكاتبات، وتنظيم سير العمل الإداري.'
  }
  
  // مدير قسم
  if (positionLower.includes('مدير قسم') || positionLower.includes('مدير بند')) {
    return 'الإشراف على جميع أنشطة القسم، إدارة فريق العمل، متابعة تنفيذ المشاريع، ضمان الجودة، وتحقيق الأهداف المحددة.'
  }
  
  // الموارد البشرية
  if (positionLower.includes('موارد بشرية') || positionLower === 'hr') {
    return 'إدارة شؤون الموظفين، التوظيف والتعيين، متابعة الحضور والانصراف، إعداد كشوف المرتبات، وحل المشكلات الإدارية للموظفين.'
  }
  
  // شؤون قانونية
  if (positionLower.includes('قانون') || positionLower.includes('مستشار')) {
    return 'تقديم الاستشارات القانونية، مراجعة العقود والاتفاقيات، متابعة القضايا القانونية، وضمان التزام الشركة بالقوانين واللوائح.'
  }
  
  // Social Media
  if (positionLower.includes('social media') || positionLower.includes('سوشيال ميديا')) {
    return 'إدارة حسابات التواصل الاجتماعي، إنشاء المحتوى الإبداعي، التفاعل مع العملاء، تحليل الأداء، وتنفيذ الحملات التسويقية.'
  }
  
  // Producer / Photographer
  if (positionLower.includes('producer') || positionLower.includes('photographer')) {
    return 'إنتاج المحتوى المرئي والمصور، التصوير الفوتوغرافي والفيديو، المونتاج والإخراج، وإنتاج المواد الإعلانية.'
  }
  
  // AI Developer
  if (positionLower.includes('ai developer') || positionLower.includes('مطور')) {
    return 'تطوير حلول الذكاء الاصطناعي، برمجة الأنظمة الذكية، تحسين العمليات باستخدام التقنيات الحديثة، وتطوير الأدوات التقنية.'
  }
  
  // Content Creator / Moderator
  if (positionLower.includes('content') || positionLower.includes('moderator')) {
    return 'إنشاء المحتوى الإبداعي، كتابة النصوص والمقالات، إدارة التعليقات والتفاعل، ومراقبة جودة المحتوى المنشور.'
  }
  
  // عمال المخزن والتشوينات
  if (positionLower.includes('عامل مخزن') || positionLower.includes('عامل تشوينات')) {
    return 'استلام وتخزين المواد، ترتيب وتنظيم المخزن، جرد المخزون، تحميل وتفريغ البضائع، والمحافظة على نظافة وترتيب المخزن.'
  }
  
  // حارس
  if (positionLower.includes('حارس') || positionLower.includes('غفير')) {
    return 'حراسة وتأمين الموقع، مراقبة الدخول والخروج، المحافظة على الممتلكات، والإبلاغ عن أي مشاكل أمنية.'
  }
  
  // مهندسين الشركة
  if (positionLower.includes('مهندس') && !positionLower.includes('مدير')) {
    if (positionLower.includes('جودة')) {
      return 'فحص ومراجعة جودة الأعمال المنفذة، التأكد من مطابقة المواصفات، كتابة تقارير الجودة، ومتابعة تصحيح الأخطاء.'
    }
    return 'الإشراف على تنفيذ المشاريع في المنطقة المخصصة، متابعة المقاولين، مراجعة الأعمال، حل المشاكل الفنية، وضمان الالتزام بالمواصفات والجداول الزمنية.'
  }
  
  // المعاينات والمشتريات
  if (positionLower.includes('معاينات') || positionLower.includes('مشتريات') || positionLower.includes('خدمات')) {
    return 'إجراء المعاينات للوحدات، شراء المواد والمستلزمات، التفاوض مع الموردين، متابعة الطلبات، وضمان توفر المواد في الوقت المناسب.'
  }
  
  // الحسابات
  if (positionLower.includes('محاسب') || positionLower.includes('حسابات')) {
    if (positionLower.includes('خزينة')) {
      return 'إدارة الخزينة، استلام وصرف النقدية، تسجيل العمليات المالية، إعداد التقارير اليومية، والمحافظة على أمان الأموال.'
    }
    if (positionLower.includes('مخزن')) {
      return 'محاسبة المخزون، تسجيل حركة المواد، إعداد تقارير المخزن، مطابقة الفواتير، ومتابعة الجرد.'
    }
    if (positionLower.includes('إضافات')) {
      return 'حساب وتسجيل الإضافات للعملاء، إعداد الفواتير، متابعة المستحقات، والتنسيق مع العملاء لتسوية الحسابات.'
    }
    return 'تسجيل العمليات المالية، إعداد القيود المحاسبية، مراجعة الفواتير، إعداد التقارير المالية، ومتابعة الحسابات.'
  }
  
  // النجارة
  if (positionLower.includes('نجار') || positionLower.includes('نجارة')) {
    if (positionLower.includes('جودة')) {
      return 'فحص جودة أعمال النجارة، التأكد من المطابقة للمواصفات، كتابة تقارير الجودة، ومتابعة تصحيح العيوب.'
    }
    if (positionLower.includes('تصميم')) {
      return 'تصميم قطع الأثاث والنجارة، إعداد الرسومات التنفيذية، اختيار الخامات والألوان، والتنسيق مع العملاء.'
    }
    if (positionLower.includes('سكرتير')) {
      return 'تنظيم مواعيد القسم، استقبال العملاء، تنسيق الاجتماعات، متابعة المراسلات، وإعداد التقارير الإدارية.'
    }
    if (positionLower.includes('خدمة عملاء')) {
      return 'التواصل مع العملاء، الرد على الاستفسارات، متابعة الطلبات، حل المشاكل، وضمان رضا العملاء.'
    }
    if (positionLower.includes('سواق')) {
      return 'نقل المواد والمعدات، توصيل الطلبات، المحافظة على السيارة، والالتزام بمواعيد التسليم.'
    }
    return 'تصنيع وتركيب قطع النجارة، قراءة الرسومات، استخدام المعدات، والمحافظة على جودة العمل.'
  }
  
  // المكتب الفني
  if (positionLower.includes('مكتب فني')) {
    return 'إعداد الرسومات التنفيذية، حساب الكميات، إعداد المقايسات، متابعة المشاريع فنياً، وتقديم الدعم الفني للمهندسين.'
  }
  
  // خدمة العملاء
  if (positionLower.includes('خدمة عملاء') || positionLower.includes('خدمه عملاء')) {
    return 'التواصل مع العملاء، الرد على الاستفسارات والشكاوى، متابعة الطلبات، تنسيق المواعيد، وضمان رضا العملاء.'
  }
  
  // الفرش والديكور
  if (positionLower.includes('فرش') || positionLower.includes('ديكور')) {
    if (positionLower.includes('3d')) {
      return 'إنشاء التصاميم ثلاثية الأبعاد، عمل الرندر والإخراج النهائي، تقديم التصورات للعملاء، والتعديل حسب الطلب.'
    }
    if (positionLower.includes('سيلز')) {
      return 'عرض المنتجات على العملاء، تقديم الاستشارات، إتمام عمليات البيع، متابعة الطلبات، وتحقيق الأهداف البيعية.'
    }
    return 'تصميم الديكورات الداخلية، اختيار الأثاث والإكسسوارات، التنسيق مع العملاء، وإعداد المقترحات التصميمية.'
  }
  
  // التعاقدات
  if (positionLower.includes('تعاقدات')) {
    if (positionLower.includes('سيلز')) {
      return 'التواصل مع العملاء الجدد، عرض الباقات والخدمات، إتمام التعاقدات، متابعة الدفعات، وتحقيق الأهداف البيعية.'
    }
    return 'إعداد العقود والاتفاقيات، متابعة التوقيعات، تنظيم ملفات العملاء، متابعة الدفعات، والتنسيق مع الأقسام الأخرى.'
  }
  
  // تلي سيلز
  if (positionLower.includes('تلي سيلز') || positionLower.includes('telesales')) {
    return 'التواصل الهاتفي مع العملاء المحتملين، عرض الخدمات والباقات، حجز المواعيد، متابعة العملاء، وتحقيق الأهداف البيعية.'
  }
  
  // السيراميك
  if (positionLower.includes('سيراميك') || positionLower.includes('منسق')) {
    return 'التنسيق مع العملاء لاختيار السيراميك، عرض العينات، حساب الكميات، متابعة التوريد والتركيب، وضمان رضا العملاء.'
  }
  
  // التشغيل
  if (positionLower.includes('تشغيل') || positionLower.includes('مشاريع')) {
    if (positionLower.includes('مقاولين')) {
      return 'التنسيق مع المقاولين، متابعة تنفيذ الأعمال، حل المشاكل، التأكد من الالتزام بالجداول الزمنية، وضمان جودة التنفيذ.'
    }
    return 'الإشراف على تنفيذ المشاريع، إدارة فرق العمل، متابعة الجداول الزمنية، حل المشاكل التشغيلية، وضمان سير العمل بكفاءة.'
  }
  
  // البوفيه
  if (positionLower.includes('بوفيه')) {
    return 'إعداد وتقديم المشروبات والوجبات الخفيفة، المحافظة على نظافة البوفيه، إدارة المخزون، وخدمة الموظفين والضيوف.'
  }
  
  // معارض
  if (positionLower.includes('معرض')) {
    return 'إدارة المعرض، عرض المنتجات، استقبال العملاء، تقديم الاستشارات الفنية، إتمام عمليات البيع، وإدارة المخزون.'
  }
  
  // IT
  if (positionLower.includes('it') || positionLower.includes('تكنولوجيا المعلومات')) {
    return 'إدارة وصيانة الأنظمة التقنية، حل المشاكل التقنية، تطوير الحلول البرمجية، إدارة الشبكات، وتقديم الدعم الفني.'
  }
  
  // Default
  return 'تنفيذ المهام الموكلة بكفاءة، التعاون مع فريق العمل، الالتزام بمعايير الجودة، والمساهمة في تحقيق أهداف القسم.'
}
